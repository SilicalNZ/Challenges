func TestSnakePath_Basic(t *testing.T) {
	game := &Game{
		MyID: 0,
		Me:   &Player{},
		Them: &Player{},
		Grid: &Grid{
			Width:  5,
			Height: 5,
			Cells: [][]GridCell{
				{"#", " ", " ", " ", "#"},
				{"#", " ", " ", " ", "#"},
				{"#", " ", " ", " ", "#"},
				{"#", " ", " ", " ", "#"},
				{"#", " ", " ", " ", "#"},
			},
		},
	}

	startSnake := []Coordinate{
		{X: 1, Y: 1},
		{X: 1, Y: 2},
	}

	req := &SnakePathReq{
		Goal:        Coordinate{X: 1, Y: 3},
		SnakeCoords: startSnake,
		Path:        startSnake,
		Depth:       0,
	}

	dir, path := game.SnakePath(req)

	if dir == "RIGHT" {
		t.Fatal("expected a direction, got none")
	}

	if len(path) == 0 {
		t.Fatal("expected a path, got empty")
	}

	last := path[len(path)-1]
	if last != req.Goal {
		t.Fatalf("expected goal %v, got %v", req.Goal, last)
	}
}

